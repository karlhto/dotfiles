-- For general setup
local servers = {
  'clangd',
  'rust_analyzer',
  'pyright',
  'tsserver',
  'vimls',
  'bashls',
  'yamlls',
  'hls',
  'jsonls',
  'kotlin_language_server'
}

for _, lsp in ipairs(servers) do
  nvim_lsp[lsp].setup {
    on_attach = on_attach,
    capabilities = capabilities
  }
end

